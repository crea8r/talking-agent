export function pickRandomThinkingPromptPhrase(random = Math.random) {
  const index = Math.max(
    0,
    Math.min(
      THINKING_PROMPT_PHRASES.length - 1,
      Math.floor(random() * THINKING_PROMPT_PHRASES.length),
    ),
  );
  return THINKING_PROMPT_PHRASES[index] || THINKING_PROMPT_PHRASES[0];
}
